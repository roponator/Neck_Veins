		private Vector3f normalid(Point3f p){
			float f = ii.globalFunctionValue(p);
			Vector3f v = new Vector3f();
		    v.x = ii.globalFunctionValue(new Point3f(p.x+diff, p.y, p.z))-f;
		    v.y = ii.globalFunctionValue(new Point3f(p.x, p.y+diff, p.z))-f;
		    v.z = ii.globalFunctionValue(new Point3f(p.x, p.y, p.z+diff))-f;
		    f = (float) Math.sqrt(v.x*v.x + v.y*v.y + v.z*v.z);
		    
		    if (f != 0.0) {
		    	f = 1/f;
		    	v.x *= f;
		    	v.y *= f; 
		    	v.z *= f;
		    }
		    return v;
		}
